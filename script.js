document.addEventListener('DOMContentLoaded', () => {
    // Initialize Map
    const map = L.map('map', {
        center: [39.0, 35.5],
        zoom: 6,
        zoomControl: false,
        minZoom: 5
    });

    // Add Map Tiles
    L.tileLayer('https://{s}.basemaps.cartocdn.com/rastertiles/voyager/{z}/{x}/{y}{r}.png', {
        attribution: '&copy; <a href="https://www.openstreetmap.org/copyright">OpenStreetMap</a> contributors &copy; <a href="https://carto.com/attributions">CARTO</a>',
        subdomains: 'abcd',
        maxZoom: 20
    }).addTo(map);

    // Zoom control
    L.control.zoom({
        position: 'bottomright'
    }).addTo(map);

    // Modal Elements
    const modal = document.getElementById('info-modal');
    const closeModalBtn = document.getElementById('close-modal');
    const cityNameEl = document.getElementById('city-name');
    const writersContentEl = document.getElementById('writers-content');
    const productsContentEl = document.getElementById('products-content');

    // About Modal Elements
    const aboutModal = document.getElementById('about-modal');
    const aboutBtn = document.getElementById('about-btn');
    const closeAboutBtn = document.getElementById('close-about');

    // Open About Modal
    aboutBtn.addEventListener('click', () => {
        aboutModal.classList.remove('hidden');
    });

    // Close About Modal
    closeAboutBtn.addEventListener('click', () => {
        aboutModal.classList.add('hidden');
    });

    // Close About Modal on Outside Click
    aboutModal.addEventListener('click', (e) => {
        if (e.target === aboutModal) {
            aboutModal.classList.add('hidden');
        }
    });

    // Helper function to create content list
    function createContentList(items, isPerson = false) {
        if (!items || items.length === 0) return document.createTextNode("Bilgi bulunamadı.");

        const listContainer = document.createElement('div');
        listContainer.className = 'detail-list';

        items.forEach(item => {
            const itemBlock = document.createElement('div');
            itemBlock.className = 'detail-item';

            if (item.image || isPerson) {
                const imgEl = document.createElement('img');
                imgEl.src = item.image ? item.image : `https://ui-avatars.com/api/?name=${encodeURIComponent(item.name)}&background=random&color=fff&size=128`;
                imgEl.alt = item.name;
                imgEl.className = 'detail-image';
                itemBlock.appendChild(imgEl);
            }

            const textContainer = document.createElement('div');
            textContainer.className = 'detail-text';

            const nameEl = document.createElement('h4');
            nameEl.textContent = item.name;
            nameEl.className = 'detail-name';

            const infoEl = document.createElement('p');
            infoEl.textContent = item.info;
            infoEl.className = 'detail-info';

            textContainer.appendChild(nameEl);
            textContainer.appendChild(infoEl);
            itemBlock.appendChild(textContainer);
            listContainer.appendChild(itemBlock);
        });

        return listContainer;
    }

    // Function to open modal
    function openModal(cityName) {
        const data = provinceData[cityName];

        if (!data) return;

        cityNameEl.textContent = cityName;

        // Populate Writers
        writersContentEl.innerHTML = '';
        writersContentEl.appendChild(createContentList(data.writers, true));

        // Populate Products
        productsContentEl.innerHTML = '';
        productsContentEl.appendChild(createContentList(data.products, false));

        modal.classList.remove('hidden');
    }

    // Close Modal Event
    closeModalBtn.addEventListener('click', () => {
        modal.classList.add('hidden');
        // Clear content slightly after close for clean reopen
        setTimeout(() => {
            writersContentEl.innerHTML = '';
            productsContentEl.innerHTML = '';
        }, 300);
    });

    // Close modal on outside click
    modal.addEventListener('click', (e) => {
        if (e.target === modal) {
            modal.classList.add('hidden');
        }
    });

    // Add City Markers
    cityCoordinates.forEach(city => {
        const marker = L.circleMarker([city.lat, city.lng], {
            radius: 12,
            fillColor: "#D4AF37", // Gold
            color: "#fff",
            weight: 2,
            opacity: 1,
            fillOpacity: 0.8
        }).addTo(map);

        marker.bindTooltip(city.name, {
            permanent: false,
            direction: 'top',
        });

        marker.on('click', () => {
            openModal(city.name);
        });
    });

    // Handle mobile responsiveness
    setTimeout(() => {
        map.invalidateSize();
    }, 100);

    // District Markers Management
    const districtMarkers = [];

    // Function to create district markers
    function createDistrictMarkers() {
        denizliDistricts.forEach(district => {
            const marker = L.circleMarker([district.lat, district.lng], {
                radius: 8,
                fillColor: "#e67e22", // Different color for districts (Orange)
                color: "#fff",
                weight: 2,
                opacity: 1,
                fillOpacity: 0.9
            });

            marker.bindTooltip(district.name, {
                permanent: true, // Always show name for districts when visible
                direction: 'top',
                className: 'district-tooltip'
            });

            marker.on('click', () => {
                openModal(district.name);
            });

            districtMarkers.push(marker);
        });
    }

    createDistrictMarkers();

    // Zoom event to toggle district markers
    map.on('zoomend', () => {
        const currentZoom = map.getZoom();

        // Show districts if zoom level is 9 or higher (zoomed in)
        // Denizli is around zoom 6-7 usually, so 9 requires user to zoom in
        if (currentZoom >= 9) {
            districtMarkers.forEach(marker => {
                if (!map.hasLayer(marker)) {
                    marker.addTo(map);
                }
            });
        } else {
            districtMarkers.forEach(marker => {
                if (map.hasLayer(marker)) {
                    marker.remove();
                }
            });
        }
    });
});
